package patterns.creation;

public abstract class Shape implements Cloneable {
    static final String TYPE_CIRCLE = "Circle";
    static final String TYPE_SQUARE = "Square";
    static final String TYPE_RECTANGLE = "Rectangle";

    private String id;
    protected String type;

    abstract void draw();

    public String getType() {
        return type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public Object clone() {
        Object clone = null;
        try {
            clone = super.clone();
        } catch (CloneNotSupportedException e) {
            e.printStackTrace();
        }
        return clone;
    }
}
